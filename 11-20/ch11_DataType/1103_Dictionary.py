# Map or Dictionary
d = {'Peter': 'iPhone-8', 'Irene': {'Samsung', 'Google'}, 'Jessica': 'iPhone-6', 'Jason': 'iPhone-7', 
'Jasmine': {'iPhone-5', 'Samsung'}, 'Jonathan': 'Verizon'}
print ('d.keys(): ', d.keys())
print ('d.values(): ', d.values())
print ('d[Peter]:', d['Peter'], 'd[Irene]', d['Irene'])